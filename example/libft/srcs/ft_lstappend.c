#include "libft.h"

void	ft_lstappend(t_list **alst, t_list *new)
{
	t_list	*buf;

	if (alst && new)
	{
		buf = *alst;
		while (buf->next)
			buf = buf->next;
		new->next = NULL;
		buf->next = new;
	}
}
